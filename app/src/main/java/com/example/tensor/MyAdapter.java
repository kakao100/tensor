    public MyAdapter(Context context){
        this.context=context;
        this.layoutInflater = (LayoutInflater)context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
    }

    public void setList(ArrayList<Mons_data> mons_data_list){ this.mons_data_list=mons_data_list; }

    @Override
    public int getCount() {
        return mons_data_list.size();
    }


    @Override
    public Object getItem(int i) {
        return mons_data_list.get(i);
    }

    @Override
    public long getItemId(int i) {
        return mons_data_list.get(i).getid();
    }

    @Override
    public View getView(int i, View view, ViewGroup viewGroup) {
        view = layoutInflater.inflate(R.layout.list_items,viewGroup,false);
        Mons_data monster=mons_data_list.get(i);

        ImageView monster_icon = (ImageView)view.findViewById(R.id.monster_icon);
        monster_icon.setImageResource(image[monster.getid()]);

        ImageView awa1 = (ImageView)view.findViewById(R.id.awa1View);
        awa1.setImageResource(awaimage[1]);
        ((TextView)view.findViewById(R.id.nameView)).setText(monster.getname());
        ((TextView)view.findViewById(R.id.statusView)).setText("HP "+String.valueOf(monster.gethp())+"  "+String.valueOf(monster.getattack())+"  "+String.valueOf(monster.getcure()));
        if(monster.gethp_110()!=0) {
            ((TextView)view.findViewById(R.id.status110View)).setText("(HP "+String.valueOf(monster.gethp_110())+"  "+String.valueOf(monster.getattack_110())+"  "+String.valueOf(monster.getcure_110())+")");
        }
        if(monster.getshortest_tern()!=0) {
            ((TextView) view.findViewById(R.id.ternView)).setText("ST " + String.valueOf(monster.getshortest_tern())+"(" + String.valueOf(monster.getlongest_tern()) + ")");
        }
        if(monster.getinheritance()) {
            ((TextView) view.findViewById(R.id.inheritance)).append("");
        }
        else {
            ((TextView) view.findViewById(R.id.inheritance)).append("-");
        }
        ((TextView) view.findViewById(R.id.skill_name)).setText(monster.getskill_name());
        ((TextView) view.findViewById(R.id.skill_exp)).setText(monster.getskill_exp());
        ((TextView)view.findViewById(R.id.rareView)).setText(" "+String.valueOf(monster.getrare()));
        return view;
    }


